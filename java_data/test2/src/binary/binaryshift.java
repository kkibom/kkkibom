package binary;

public class binaryshift {
    public static void main(String[] args) {

        int i =13;
        System.out.println("i<<1 :" + (i<< 1));
        System.out.println("i<<2 :" + (i<< 2));
        System.out.println("i>>2 :" + (i>> 2));
        System.out.println("i>>1 :" + (i>> 1));


    }
}
